public class ExampleStuff {
    private String name;
    private int age;
    private double[] dd;

    public ExampleStuff(String name, int age, double[] dd) {
        this.name = name;
        this.age = age;
        this.dd = dd;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public double[] getDd() {
        return dd;
    }
}
